public class StringsQues2 {
    public static void main(String[] args) {
        String str1 = "Hello";
        String str2 = "World";
        
        // Concatenating strings using + operator
        String result = str1 + " " + str2;
        
        System.out.println("Concatenated string: " + result);
    }
}
