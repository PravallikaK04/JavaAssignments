public class OperatorsQuestion6 {
    public static void checkRelationalOperators(int a, int b) {
        System.out.println("a < b: " + (a < b));
        System.out.println("a <= b: " + (a <= b));
        System.out.println("a > b: " + (a > b));
        System.out.println("a >= b: " + (a >= b));
    }

    public static void main(String[] args) {
        checkRelationalOperators(10, 5);
    }
}
