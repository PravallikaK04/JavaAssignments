public class LoopsQuestion11 {
    public static void main(String[] args) {
        int num = 5;
        switch (num % 2) {
            case 0:
                System.out.println(num + " is EVEN");
                break;
            case 1:
                System.out.println(num + " is ODD");
                break;
        }
    }
}
