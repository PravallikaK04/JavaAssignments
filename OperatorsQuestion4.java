public class OperatorsQuestion4 {
    public static void checkIfEqual(int a, int b) {
        if (a == b) {
            System.out.println("Numbers are equal");
        } else {
            System.out.println("Numbers are not equal");
        }
    }

    public static void main(String[] args) {
        checkIfEqual(10, 5);
    }
}
